    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:
        m.setNim(txtnim.getText());
        if("".equals(txtnama.getText()) && "".equals(txtnim.getText())){
            JOptionPane.showMessageDialog(rootPane, "Isi Datanya Dulu", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            m.setNama(txtnama.getText());
            m.setFakultas(txtfakultas.getText());
            md.AddMahasiswa(m);
            txtnim.setText("");
            txtnama.setText("");
            txtfakultas.setText("");
        }
    }//GEN-LAST:event_addActionPerformed

