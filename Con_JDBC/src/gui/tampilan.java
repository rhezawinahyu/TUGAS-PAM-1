    public void binding(){
        ListMhs = md.getAll();
        Object[][] data = new Object[ListMhs.size()][3];
        int i = 0;
        for(Mahasiswa mhs : ListMhs){
            data[i][0] = mhs.getNim();
            data[i][1] = mhs.getNama();
            data[i][2] = mhs.getFakultas();
            i++;
            jTable1.setModel(new DefaultTableModel(data, new String[]{"NIM", "Nama","Fakultas"}));
        }
    }
